const RADIUS = 8
const MARGIN_TOP = 60
const MARGIN_LEFT = 30

export const render = (ctx: CanvasRenderingContext2D, currentTime: Date) => {
  ctx.clearRect(0, 0, ctx.canvas.width, ctx.canvas.height)
  const r = RADIUS
  const h = currentTime.getHours()
  const m = currentTime.getMinutes()
  const s = currentTime.getSeconds()

  renderNumber(MARGIN_LEFT, MARGIN_TOP, Math.floor(h / 10), ctx)
  renderNumber(MARGIN_LEFT + 15 * (r + 1), MARGIN_TOP, Math.floor(h % 10), ctx)
  renderNumber(MARGIN_LEFT + 30 * (r + 1), MARGIN_TOP, 10, ctx)
  renderNumber(MARGIN_LEFT + 39 * (r + 1), MARGIN_TOP, Math.floor(m / 10), ctx)
  renderNumber(MARGIN_LEFT + 54 * (r + 1), MARGIN_TOP, Math.floor(m % 10), ctx)
  renderNumber(MARGIN_LEFT + 69 * (r + 1), MARGIN_TOP, 10, ctx)
  renderNumber(MARGIN_LEFT + 78 * (r + 1), MARGIN_TOP, Math.floor(s / 10), ctx)
  renderNumber(MARGIN_LEFT + 93 * (r + 1), MARGIN_TOP, Math.floor(s % 10), ctx)

  window.requestAnimationFrame(() => {
    render(ctx, new Date())
  })
}

const renderNumber = (
  x: number,
  y: number,
  num: number,
  ctx: CanvasRenderingContext2D
) => {
  const r = RADIUS
  let rx = 0
  let ry = 0
  for (let i = 0; i < countdownParam[num].length; i++) {
    for (let j = 0; j < countdownParam[num][i].length; j++) {
      const point = countdownParam[num][i][j]
      if (point === 1) {
        rx = x + (2 * j + 1) * (r + 1)
        ry = y + (2 * i + 1) * (r + 1)
        ctx.beginPath()
        ctx.arc(rx, ry, r, 0, 2 * Math.PI)
        ctx.closePath()

        ctx.fillStyle = 'rgb(0, 102, 153)'
        ctx.fill()
      }
    }
  }
}
